package com.hrbb.loan.pos.integration.common.gongshangju.constants;
/**
 * 认证类型
 * 
 * @author XLY
 * @version $Id: AuthTypeEnum.java, v 0.1 2015-2-17 下午1:07:25 XLY Exp $
 */
public enum AuthKeyTypeEnum {

    AUTH_BY_NAME                  ("2", "企业 名称全认证"), 
    AUTH_BY_LICENCE               ("3", "数据不存在"),
    AUTH_BY_CERT                  ("4", "自然人 证件号码认证"),
    ;
    private String code;
    private String message;
    
    /**
     * @param code
     * @param message
     */
    private AuthKeyTypeEnum(String code, String message) {
        this.code = code;
        this.message = message;
    }
    

    /**
     * Getter method for property <tt>code</tt>.
     * 
     * @return property value of code
     */
    public String getCode() {
        return code;
    }

    /**
     * Setter method for property <tt>code</tt>.
     * 
     * @param code value to be assigned to property code
     */
    public void setCode(String code) {
        this.code = code;
    }

    /**
     * Getter method for property <tt>message</tt>.
     * 
     * @return property value of message
     */
    public String getMessage() {
        return message;
    }
}
