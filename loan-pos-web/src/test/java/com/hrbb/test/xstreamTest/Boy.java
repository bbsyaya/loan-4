package com.hrbb.test.xstreamTest;

import com.thoughtworks.xstream.annotations.XStreamAlias;

@XStreamAlias("BOY")
public class Boy {
	
	@XStreamAlias("NAME")
	private String name;
	
	@XStreamAlias("AGE")
	private String age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}
	
	
}
