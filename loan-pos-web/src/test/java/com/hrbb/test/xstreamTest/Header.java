package com.hrbb.test.xstreamTest;

import com.thoughtworks.xstream.annotations.XStreamAlias;

@XStreamAlias("header")
public class Header {

	@XStreamAlias("tx_client")
	private String txClientString;
	
	@XStreamAlias("tx_seq")
	private String txSeq;
	
	@XStreamAlias("tx_date")
	private String txDate;
	
	@XStreamAlias("tx_time")
	private String txTime;

	public String getTxClientString() {
		return txClientString;
	}

	public void setTxClientString(String txClientString) {
		this.txClientString = txClientString;
	}

	public String getTxSeq() {
		return txSeq;
	}

	public void setTxSeq(String txSeq) {
		this.txSeq = txSeq;
	}

	public String getTxDate() {
		return txDate;
	}

	public void setTxDate(String txDate) {
		this.txDate = txDate;
	}

	public String getTxTime() {
		return txTime;
	}

	public void setTxTime(String txTime) {
		this.txTime = txTime;
	}
	
	
}
