	public static final int CUST_INV_MOBILE = 0x0009;
	public static final int CUST_INV_CIV_FRONT = 0x000A;
	public static final int CUST_INV_CIV_BAK = 0x000B;
	public static final int CUST_INV = 0x000C;
	public static final int CUST_ALREADY_EXISTS = 0x000D;
	public static final int CUST_CONTRACT_EXISTS = 0x000E;
	
	public static final int APPLY_NOT_FOUND = 0x0011;
	public static final int APPLY_INVALID = 0x0012;
	public static final int APPLY_INV_BANK_CARD_NO = 0x0013;
	public static final int APPLY_INV_PERIODS = 0x0014;
