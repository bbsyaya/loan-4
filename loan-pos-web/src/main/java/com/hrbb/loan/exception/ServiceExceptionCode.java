package com.hrbb.loan.exception;

/**
 * response code separate in every 4 digits
 * <p> 
 * 0 represents successful
 * 1 represents not exist in request
 * 2 represents exist in request, but not valid
 * 
 * 
 * @author Aaron
 *
 */
