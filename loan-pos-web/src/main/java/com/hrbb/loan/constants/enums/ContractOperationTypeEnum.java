package com.hrbb.loan.constants.enums;

/**
 * 合同操作类型枚举
 * 
 * @author cjq
 * @version $Id: ContractOperationTypeEnum.java, v 0.1 2015年7月22日 上午11:53:39 cjq Exp $
 */
public enum ContractOperationTypeEnum {
    
    SX("SX","协议生效"),
    DJ("DJ","协议冻结"),
    JD("JD","协议解冻"),
    TZ("TZ","协议调整"),
    CQ("CQ","退回重签");
    
    private String code;
    private String message;

    private ContractOperationTypeEnum(String code,String message){
        this.code = code;
        this.message = message;
    }
    
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
