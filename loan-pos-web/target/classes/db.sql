DROP TABLE IF EXISTS `t_callback_record`;
CREATE TABLE IF NOT EXISTS `t_callback_record`(
    id int(11) primary key auto_increment,
    loan_id varchar(40) default null comment '授信贷款编号',
    cont_no varchar(50) default null comment '合同编号',
    respCode varchar(32) default null comment '返回码',
    created_at datetime comment '创建时间',
    updated_at datetime comment '更新时间'
);

DROP TABLE IF EXISTS `t_fixLine_info`;
CREATE TABLE `t_fixLine_info`(
 `id` int(11) PRIMARY KEY AUTO_INCREMENT,
`telNum` VARCHAR(30) COMMENT '公司电话',
`name` VARCHAR(50) COMMENT '公司名称',
`address` VARCHAR(100) COMMENT '公司地址',
`version` VARCHAR(100) COMMENT '版本',
`telInput` VARCHAR(30) COMMENT '输入电话',
`nameInput` VARCHAR(50) COMMENT '输入名称',
`addressInput` VARCHAR(100) COMMENT '输入地址',
`uniqueNum` VARCHAR(200) COMMENT '唯一标识',
`uniqueNumDesc` VARCHAR(50) COMMENT '唯一标识描述',
`areaCode` VARCHAR(50) COMMENT '区域名称',
`queryResult` VARCHAR(30) COMMENT '查询结果描述',
`queryType` VARCHAR(30) COMMENT '查询类型',
`corpTel` VARCHAR(30) COMMENT '查得电话',
`corpName` VARCHAR(50) COMMENT '查得名称',
`corpAddress` VARCHAR(100) COMMENT '查得地址',
`created_at` datetime comment '创建时间',
`updated_at` datetime comment '更新时间'
)
